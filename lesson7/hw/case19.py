def number_to_text(n):
    if n < 100 or n > 999:
        return "Invalid number. It should be a three-digit number."

    units = ["", "як", "ду", "се", "чор", "панч", "шаш", "хафт", "хашт", "нух"]
    teens = ["дах", "ёздах", "дувоздах", "сенздах", "чордах", "понздах", "шонздах", "хабдах", "хаждах", "нуздах"]
    tens = ["", "дах", "бист", "си", "чил", "панчох", "шаст", "хафтод", "хаштод", "навад"]
    hundreds = ["", "яксад", "дусад", "сесад", "чорсад", "панчсад", "шашсад", "хафтсад", "хашсад", "нухсад"]

    h = n // 100
    t = (n % 100) // 10
    u = n % 10

    result = hundreds[h]

    if t == 1:
        result += " " + teens[u]
    else:
        if t > 0:
            result += "у " + tens[t]
        if u > 0:
            result += "у " + units[u]

    return result.strip()

# Примеры использования
numbers = [256, 814, 100, 999, 305, 720, 118]
for number in numbers:
    print(f"{number}: {number_to_text(number)}")

